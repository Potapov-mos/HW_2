-- 2. Для данных таблицы “sales” укажите тип заказа в зависимости от кол-ва : меньше 100 - 
-- Маленький заказ, от 100 до 300 - Средний заказ, больше 300 - Большой заказ
-- с использованием CASE


USE HW_2;

SELECT id, 'id заказа',
CASE 
	WHEN order_cnt < 100 THEN 'Маленький заказ'
	WHEN order_cnt >= 100 AND order_cnt <= 300 THEN 'Средний заказ'
	ELSE 'Большой заказ'
END AS 'Тип заказа'
FROM sales;